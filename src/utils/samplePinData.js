var numberOfSensors = 10;
var sensors = [];
for (var i = 1; i <= numberOfSensors; i++) {
  var dataLength = Math.floor(Math.random() * 10) + 5; // Random data array length (between 1 and 5)
  var data = [];
  var currentDate = new Date(); // Start with the current date
  for (var j = 0; j < dataLength; j++) {
    // Calculate the timestamp with a difference of weeks or months
    var ts = new Date(currentDate.getTime());
    if (j > 0) {
      // Add a difference of weeks or months
      ts.setMonth(ts.getMonth() + Math.floor(Math.random() * 2) + 1); // 1 or 2 months
      ts.setDate(ts.getDate() + Math.floor(Math.random() * 14) + 1); // 1 to 14 days
    } // Convert the timestamp to a string
    data.push({
      ts,
      t: (Math.random() * 10 + 20).toFixed(1),
      h: (Math.random() * 20 + 30).toFixed(1),
      p: (Math.random() * 10 + 1000).toFixed(1),
      l: (Math.random() * 100 + 100).toFixed(1),
      d: (Math.random() * 100).toFixed(1),
    });
    // Update the current date for the next data point
    currentDate.setTime(ts.getTime());
  }
  var sensor = {
    id: i,
    name: "Sensor ".concat(i),
    data: data,
    used: false,
    radius: 200,
  };
  sensors.push(sensor);
}
